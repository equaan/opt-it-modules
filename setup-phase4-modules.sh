# ================================================================
# ${{ values.client_name }} — Node.js Dockerfile
# Generated by Opt IT Backstage — DO NOT EDIT MANUALLY
#
# Multi-stage build:
#   Stage 1 (builder) — install dependencies and build
#   Stage 2 (production) — minimal runtime image
# ================================================================

# ── STAGE 1: Builder ─────────────────────────────────────────────
FROM node:${{ values.runtime_version }}-alpine AS builder

WORKDIR /app

# Copy package files first for better layer caching
COPY package*.json ./

# Install all dependencies including devDependencies for build
RUN npm ci

# Copy source code
COPY . .

# Build the application
RUN npm run build

# ── STAGE 2: Production ──────────────────────────────────────────
FROM node:${{ values.runtime_version }}-alpine AS production

WORKDIR /app

# Create non-root user for security
RUN addgroup -g 1001 -S nodejs && \
    adduser -S -u 1001 -G nodejs nodejs

# Copy package files
COPY package*.json ./

# Install production dependencies only
RUN npm ci --only=production && \
    npm cache clean --force

# Copy built application from builder stage
COPY --from=builder /app/dist ./dist

# Change ownership to non-root user
RUN chown -R nodejs:nodejs /app

USER nodejs

# Expose application port
EXPOSE ${{ values.app_port }}

# Health check
HEALTHCHECK --interval=30s --timeout=10s --start-period=30s --retries=3 \
  CMD node -e "require('http').get('http://localhost:${{ values.app_port }}${{ values.health_check_path }}', (r) => r.statusCode === 200 ? process.exit(0) : process.exit(1))"

# Start the application
CMD ["node", "dist/index.js"]
EOF

cat > containers/dockerfiles/Dockerfile.python << 'EOF'
# ================================================================
# ${{ values.client_name }} — Python Dockerfile
# Generated by Opt IT Backstage — DO NOT EDIT MANUALLY
# ================================================================

# ── STAGE 1: Builder ─────────────────────────────────────────────
FROM python:${{ values.runtime_version }}-slim AS builder

WORKDIR /app

# Install build dependencies
RUN apt-get update && apt-get install -y --no-install-recommends \
    build-essential \
    && rm -rf /var/lib/apt/lists/*

# Copy requirements first for layer caching
COPY requirements.txt .

# Install dependencies into a virtual environment
RUN python -m venv /opt/venv && \
    /opt/venv/bin/pip install --no-cache-dir --upgrade pip && \
    /opt/venv/bin/pip install --no-cache-dir -r requirements.txt

# ── STAGE 2: Production ──────────────────────────────────────────
FROM python:${{ values.runtime_version }}-slim AS production

WORKDIR /app

# Create non-root user
RUN groupadd -g 1001 appuser && \
    useradd -u 1001 -g appuser -s /bin/sh appuser

# Copy virtual environment from builder
COPY --from=builder /opt/venv /opt/venv

# Copy application code
COPY --chown=appuser:appuser . .

# Activate virtual environment
ENV PATH="/opt/venv/bin:$PATH"
ENV PYTHONDONTWRITEBYTECODE=1
ENV PYTHONUNBUFFERED=1

USER appuser

EXPOSE ${{ values.app_port }}

HEALTHCHECK --interval=30s --timeout=10s --start-period=30s --retries=3 \
  CMD python -c "import urllib.request; urllib.request.urlopen('http://localhost:${{ values.app_port }}${{ values.health_check_path }}')"

CMD ["python", "-m", "gunicorn", "--bind", "0.0.0.0:${{ values.app_port }}", "app:app"]
EOF

