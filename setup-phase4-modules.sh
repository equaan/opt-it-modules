# ================================================================
# ${{ values.client_name }} — Kubernetes Deployment
# Generated by Opt IT Backstage — DO NOT EDIT MANUALLY
# ================================================================

apiVersion: apps/v1
kind: Deployment
metadata:
  name: ${{ values.client_name }}-app
  namespace: ${{ values.k8s_namespace }}
  labels:
    app: ${{ values.client_name }}
    environment: ${{ values.environment }}
    managed-by: opt-it-backstage
spec:
  replicas: ${{ values.initial_replicas }}
  selector:
    matchLabels:
      app: ${{ values.client_name }}
  strategy:
    type: RollingUpdate
    rollingUpdate:
      maxUnavailable: 0
      maxSurge: 1
  template:
    metadata:
      labels:
        app: ${{ values.client_name }}
        environment: ${{ values.environment }}
    spec:
      # Security context — run as non-root
      securityContext:
        runAsNonRoot: true
        runAsUser: 1001
        fsGroup: 1001

      containers:
        - name: app
          image: ${{ values.container_registry }}/${{ values.client_name }}:latest
          imagePullPolicy: Always
          ports:
            - containerPort: ${{ values.app_port }}
              protocol: TCP

          # Resource limits — always set to prevent noisy neighbours
          resources:
            requests:
              cpu: ${{ values.cpu_request }}
              memory: ${{ values.memory_request }}
            limits:
              cpu: ${{ values.cpu_limit }}
              memory: ${{ values.memory_limit }}

          # Liveness probe — restarts container if unhealthy
          livenessProbe:
            httpGet:
              path: ${{ values.health_check_path }}
              port: ${{ values.app_port }}
            initialDelaySeconds: 30
            periodSeconds: 10
            failureThreshold: 3

          # Readiness probe — removes from load balancer if not ready
          readinessProbe:
            httpGet:
              path: ${{ values.health_check_path }}
              port: ${{ values.app_port }}
            initialDelaySeconds: 10
            periodSeconds: 5
            failureThreshold: 3

          # Environment variables from ConfigMap and Secret
          envFrom:
            - configMapRef:
                name: ${{ values.client_name }}-config
            - secretRef:
                name: ${{ values.client_name }}-secrets

          # Container security context
          securityContext:
            allowPrivilegeEscalation: false
            readOnlyRootFilesystem: true
            capabilities:
              drop:
                - ALL

      # Ensure pods spread across nodes
      topologySpreadConstraints:
        - maxSkew: 1
          topologyKey: kubernetes.io/hostname
          whenUnsatisfiable: DoNotSchedule
          labelSelector:
            matchLabels:
              app: ${{ values.client_name }}
EOF

