# ================================================================
# ${{ values.client_name }} — Trivy Security Scan
# Generated by Opt IT Backstage — DO NOT EDIT MANUALLY
# ================================================================

name: Security Scan — Trivy

on:
  push:
    branches: [main, develop]
  pull_request:
    branches: [main]
  schedule:
    # Run every day at 2am UTC
    - cron: '0 2 * * *'

jobs:
  trivy-fs-scan:
    name: Trivy Filesystem Scan
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Run Trivy filesystem scan
        uses: aquasecurity/trivy-action@master
        with:
          scan-type: 'fs'
          scan-ref: '.'
          trivy-config: security/trivy/trivy.yaml
          ignore-unfixed: ${{ values.ignore_unfixed }}
          severity: 'CRITICAL,HIGH${% if values.include_medium_severity %},MEDIUM${% endif %}'
          format: 'sarif'
          output: 'trivy-fs-results.sarif'

      - name: Upload Trivy filesystem scan results to GitHub Security
        uses: github/codeql-action/upload-sarif@v3
        if: always()
        with:
          sarif_file: 'trivy-fs-results.sarif'
          category: 'trivy-filesystem'

${% if values.scan_docker_image %}
  trivy-image-scan:
    name: Trivy Container Image Scan
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Build Docker image
        run: docker build -t ${{ values.client_name }}:${{ '${{ github.sha }}' }} .

      - name: Run Trivy container image scan
        uses: aquasecurity/trivy-action@master
        with:
          image-ref: '${{ values.client_name }}:${{ "${{ github.sha }}" }}'
          trivy-config: security/trivy/trivy.yaml
          ignore-unfixed: ${{ values.ignore_unfixed }}
          severity: 'CRITICAL,HIGH${% if values.include_medium_severity %},MEDIUM${% endif %}'
          format: 'sarif'
          output: 'trivy-image-results.sarif'

      - name: Upload Trivy image scan results to GitHub Security
        uses: github/codeql-action/upload-sarif@v3
        if: always()
        with:
          sarif_file: 'trivy-image-results.sarif'
          category: 'trivy-container'
${% endif %}

${% if values.scan_iac %}
  trivy-iac-scan:
    name: Trivy IaC Scan
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Run Trivy IaC scan
