# ────────────────────────────────────────────────────────────────
# DOCKER COMPOSE DEPLOYMENT
# ────────────────────────────────────────────────────────────────

cat > observability/docker-compose/docker-compose.yml << 'EOF'
# ================================================================
# ${{ values.client_name }} — Observability Stack
# Docker Compose Deployment
# Generated by Opt IT Backstage — DO NOT EDIT MANUALLY
#
# Usage:
#   docker compose up -d
#   Grafana: http://localhost:${{ values.grafana_port }}
#   Prometheus: http://localhost:9090
# ================================================================

version: "3.8"

networks:
  monitoring:
    driver: bridge

volumes:
  prometheus_data:
  grafana_data:

services:

  prometheus:
    image: prom/prometheus:v2.48.0
    container_name: ${{ values.client_name }}-prometheus
    restart: unless-stopped
    networks:
      - monitoring
    ports:
      - "9090:9090"
    volumes:
      - ./prometheus/prometheus.yml:/etc/prometheus/prometheus.yml:ro
      - ./prometheus/alert-rules:/etc/prometheus/alert-rules:ro
      - prometheus_data:/prometheus
    command:
      - "--config.file=/etc/prometheus/prometheus.yml"
      - "--storage.tsdb.path=/prometheus"
      - "--storage.tsdb.retention.time=${{ values.retention_days }}d"
      - "--web.console.libraries=/usr/share/prometheus/console_libraries"
      - "--web.console.templates=/usr/share/prometheus/consoles"
      - "--web.enable-lifecycle"
    labels:
      client: ${{ values.client_name }}
      environment: ${{ values.environment }}
      managed-by: opt-it-backstage

  grafana:
    image: grafana/grafana:10.2.0
    container_name: ${{ values.client_name }}-grafana
    restart: unless-stopped
    networks:
      - monitoring
    ports:
      - "${{ values.grafana_port }}:3000"
    volumes:
      - ./grafana/grafana.ini:/etc/grafana/grafana.ini:ro
      - ./grafana/provisioning:/etc/grafana/provisioning:ro
      - ./grafana/dashboards:/etc/grafana/dashboards:ro
      - grafana_data:/var/lib/grafana
    environment:
      GF_SECURITY_ADMIN_PASSWORD: ${{ values.grafana_admin_password }}
    depends_on:
      - prometheus
    labels:
      client: ${{ values.client_name }}
      environment: ${{ values.environment }}
      managed-by: opt-it-backstage

  node-exporter:
    image: prom/node-exporter:v1.7.0
    container_name: ${{ values.client_name }}-node-exporter
    restart: unless-stopped
    networks:
      - monitoring
    pid: host
    volumes:
      - /proc:/host/proc:ro
      - /sys:/host/sys:ro
      - /:/rootfs:ro
    command:
      - "--path.procfs=/host/proc"
      - "--path.rootfs=/rootfs"
      - "--path.sysfs=/host/sys"
      - "--collector.filesystem.mount-points-exclude=^/(sys|proc|dev|host|etc)($$|/)"
    labels:
      client: ${{ values.client_name }}
      environment: ${{ values.environment }}
      managed-by: opt-it-backstage

  alertmanager:
    image: prom/alertmanager:v0.26.0
    container_name: ${{ values.client_name }}-alertmanager
    restart: unless-stopped
    networks:
      - monitoring
    ports:
      - "9093:9093"
    volumes:
      - ./alertmanager/alertmanager.yml:/etc/alertmanager/alertmanager.yml:ro
    labels:
      client: ${{ values.client_name }}
      environment: ${{ values.environment }}
      managed-by: opt-it-backstage

  blackbox-exporter:
    image: prom/blackbox-exporter:v0.24.0
    container_name: ${{ values.client_name }}-blackbox
    restart: unless-stopped
    networks:
      - monitoring
    ports:
      - "9115:9115"
    labels:
      client: ${{ values.client_name }}
      environment: ${{ values.environment }}
      managed-by: opt-it-backstage
EOF

# ────────────────────────────────────────────────────────────────
# ALERTMANAGER CONFIG
# ────────────────────────────────────────────────────────────────

mkdir -p observability/alertmanager

cat > observability/alertmanager/alertmanager.yml << 'EOF'
# ================================================================
# ${{ values.client_name }} — Alertmanager Configuration
# Generated by Opt IT Backstage — DO NOT EDIT MANUALLY
# ================================================================

global:
  resolve_timeout: 5m

route:
  group_by: ['alertname', 'client', 'environment']
  group_wait:      30s
  group_interval:  5m
  repeat_interval: 12h
  receiver: 'default'

  routes:
    - match:
        severity: critical
      receiver: 'critical-alerts'
      repeat_interval: 1h

receivers:

  - name: 'default'
${% if values.slack_webhook %}
    slack_configs:
      - api_url: '${{ values.slack_webhook }}'
        channel: '#${{ values.slack_channel }}'
        title: '[{{ .Status | toUpper }}] {{ .GroupLabels.alertname }}'
        text: '{{ range .Alerts }}{{ .Annotations.description }}{{ end }}'
        send_resolved: true
${% else %}
    # Configure a notification channel above
    # Options: slack_configs, email_configs, pagerduty_configs
${% endif %}

  - name: 'critical-alerts'
${% if values.alert_email %}
    email_configs:
      - to: '${{ values.alert_email }}'
        subject: '[CRITICAL] {{ .GroupLabels.alertname }} — ${{ values.client_name }}'
        body: '{{ range .Alerts }}{{ .Annotations.description }}{{ end }}'
        send_resolved: true
${% endif %}
${% if values.slack_webhook %}
    slack_configs:
      - api_url: '${{ values.slack_webhook }}'
        channel: '#${{ values.slack_channel }}'
        title: '🚨 [CRITICAL] {{ .GroupLabels.alertname }}'
        text: '{{ range .Alerts }}{{ .Annotations.description }}{{ end }}'
        send_resolved: true
${% endif %}

inhibit_rules:
  - source_match:
      severity: 'critical'
    target_match:
      severity: 'warning'
    equal: ['alertname', 'instance']
EOF

echo "✅ Alertmanager config created"

# ────────────────────────────────────────────────────────────────
