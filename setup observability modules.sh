# ────────────────────────────────────────────────────────────────
# PROMETHEUS CONFIG
# ────────────────────────────────────────────────────────────────

cat > observability/prometheus/prometheus.yml << 'EOF'
# ================================================================
# ${{ values.client_name }} — Prometheus Configuration
# Generated by Opt IT Backstage — DO NOT EDIT MANUALLY
# ================================================================

global:
  scrape_interval:     ${{ values.scrape_interval }}
  evaluation_interval: ${{ values.scrape_interval }}
  external_labels:
    client:      '${{ values.client_name }}'
    environment: '${{ values.environment }}'
    managed_by:  'opt-it-backstage'

# Alertmanager configuration
alerting:
  alertmanagers:
    - static_configs:
        - targets:
            - alertmanager:9093

# Load alert rules
rule_files:
  - /etc/prometheus/alert-rules/*.yml

# ── SCRAPE CONFIGS ───────────────────────────────────────────────

scrape_configs:

  # Prometheus self-monitoring
  - job_name: 'prometheus'
    static_configs:
      - targets: ['localhost:9090']

  # Node Exporter — host metrics (CPU, memory, disk, network)
  - job_name: 'node-exporter'
    static_configs:
      - targets: ['node-exporter:9100']
    relabel_configs:
      - source_labels: [__address__]
        target_label: instance

  # Blackbox Exporter — HTTP endpoint monitoring
  - job_name: 'blackbox-http'
    metrics_path: /probe
    params:
      module: [http_2xx]
    static_configs:
      - targets:
          - http://localhost:80/health  # replace with your app health endpoint
    relabel_configs:
      - source_labels: [__address__]
        target_label: __param_target
      - source_labels: [__param_target]
        target_label: instance
      - target_label: __address__
        replacement: blackbox-exporter:9115

${% if values.scrape_app_metrics %}
  # Application metrics — customize this endpoint
  - job_name: '${{ values.client_name }}-app'
    static_configs:
      - targets: ['app:${{ values.app_metrics_port }}']
    metrics_path: '/metrics'
${% endif %}
EOF

cat > observability/prometheus/alert-rules/infra-alerts.yml << 'EOF'
# ================================================================
# ${{ values.client_name }} — Infrastructure Alert Rules
# Generated by Opt IT Backstage — DO NOT EDIT MANUALLY
# ================================================================

groups:
  - name: infrastructure
    rules:

      # CPU
      - alert: HighCPUUsage
        expr: 100 - (avg by(instance) (irate(node_cpu_seconds_total{mode="idle"}[5m])) * 100) > 85
        for: 5m
        labels:
          severity: warning
          client: ${{ values.client_name }}
          environment: ${{ values.environment }}
        annotations:
          summary: "High CPU usage on {{ $labels.instance }}"
          description: "CPU usage is {{ $value | humanize }}% for more than 5 minutes."

      - alert: CriticalCPUUsage
        expr: 100 - (avg by(instance) (irate(node_cpu_seconds_total{mode="idle"}[5m])) * 100) > 95
        for: 2m
        labels:
          severity: critical
          client: ${{ values.client_name }}
          environment: ${{ values.environment }}
        annotations:
          summary: "Critical CPU usage on {{ $labels.instance }}"
          description: "CPU usage is {{ $value | humanize }}% for more than 2 minutes."

      # Memory
      - alert: HighMemoryUsage
        expr: (1 - (node_memory_MemAvailable_bytes / node_memory_MemTotal_bytes)) * 100 > 85
        for: 5m
        labels:
          severity: warning
          client: ${{ values.client_name }}
          environment: ${{ values.environment }}
        annotations:
          summary: "High memory usage on {{ $labels.instance }}"
          description: "Memory usage is {{ $value | humanize }}%."

      # Disk
      - alert: DiskSpaceLow
        expr: (1 - (node_filesystem_avail_bytes{fstype!="tmpfs"} / node_filesystem_size_bytes{fstype!="tmpfs"})) * 100 > 80
        for: 5m
        labels:
          severity: warning
          client: ${{ values.client_name }}
          environment: ${{ values.environment }}
        annotations:
          summary: "Low disk space on {{ $labels.instance }}"
          description: "Disk {{ $labels.mountpoint }} is {{ $value | humanize }}% full."

      - alert: DiskSpaceCritical
        expr: (1 - (node_filesystem_avail_bytes{fstype!="tmpfs"} / node_filesystem_size_bytes{fstype!="tmpfs"})) * 100 > 90
        for: 2m
        labels:
          severity: critical
          client: ${{ values.client_name }}
          environment: ${{ values.environment }}
        annotations:
          summary: "Critical disk space on {{ $labels.instance }}"
          description: "Disk {{ $labels.mountpoint }} is {{ $value | humanize }}% full."

      # Instance down
      - alert: InstanceDown
        expr: up == 0
        for: 1m
        labels:
          severity: critical
          client: ${{ values.client_name }}
          environment: ${{ values.environment }}
        annotations:
          summary: "Instance {{ $labels.instance }} is down"
          description: "{{ $labels.instance }} has been down for more than 1 minute."
EOF

cat > observability/prometheus/alert-rules/app-alerts.yml << 'EOF'
# ================================================================
# ${{ values.client_name }} — Application Alert Rules
# Generated by Opt IT Backstage — DO NOT EDIT MANUALLY
# ================================================================

groups:
  - name: application
    rules:

      # HTTP error rate
      - alert: HighHTTPErrorRate
        expr: rate(http_requests_total{status=~"5.."}[5m]) / rate(http_requests_total[5m]) > 0.05
        for: 5m
        labels:
          severity: warning
          client: ${{ values.client_name }}
          environment: ${{ values.environment }}
        annotations:
          summary: "High HTTP error rate"
          description: "HTTP 5xx error rate is {{ $value | humanizePercentage }}."

      # HTTP latency
      - alert: HighHTTPLatency
        expr: histogram_quantile(0.99, rate(http_request_duration_seconds_bucket[5m])) > 2
        for: 5m
        labels:
          severity: warning
          client: ${{ values.client_name }}
          environment: ${{ values.environment }}
        annotations:
          summary: "High HTTP latency"
          description: "99th percentile latency is {{ $value | humanizeDuration }}."

      # Endpoint down
      - alert: EndpointDown
        expr: probe_success == 0
        for: 2m
        labels:
          severity: critical
          client: ${{ values.client_name }}
          environment: ${{ values.environment }}
        annotations:
          summary: "Endpoint {{ $labels.instance }} is down"
          description: "HTTP probe failed for {{ $labels.instance }}."
EOF

echo "✅ Prometheus configs created"

# ────────────────────────────────────────────────────────────────
# GRAFANA CONFIG
# ────────────────────────────────────────────────────────────────

